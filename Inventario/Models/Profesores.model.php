<?php
require_once('../Config/cls_conexion.model.php');
class Clase_Profesores
{
    public function todos()
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT profesores.*, escuelas.Nombre_escuela
            FROM profesores
            INNER JOIN escuelas ON profesores.ID_escuela = escuelas.ID_escuela;";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function uno($ID_profesor)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `profesores` WHERE ID_profesor=$ID_profesor";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function insertar($ID_escuela, $nombre_profesor, $materia, $salario)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "INSERT INTO `profesores`( `ID_escuela`, `nombre_profesor`, `materia`, `salario`) VALUES ('$ID_escuela','$nombre_profesor','$materia','$salario')";
            $result = mysqli_query($con, $cadena);
            return 'ok';
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function actualizar($ID_profesor, $ID_escuela, $nombre_profesor, $materia, $salario)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "UPDATE `profesores` SET `ID_escuela`='$ID_escuela',`nombre_profesor`='$nombre_profesor',`materia`='$materia',`salario`='$salario' WHERE `ID_profesor`=$ID_profesor";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function eliminar($ID_profesor)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "Delete from profesores where ID_profesor=$ID_profesor";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
}